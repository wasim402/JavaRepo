package BasicPrograms;

public class NegativePositiveNumber {
    static void main(String[] args) {
        float n =-10.1f;

        if (n>0){
            System.out.println("Positive number");
        } else if (n<0) {
            System.out.println("Negative number");
        }
        else {
            System.out.println("Number is zero");
        }
    }
}
